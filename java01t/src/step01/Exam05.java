package step01;

public class Exam05 {
  public static void main(String[] args) {
    //1) 정수값을 저장할 메모리 준비
    int i1;
    int i2, i3, i4;
    
    //2) 실수값을 저장할 메모리 준비
    double d1;
    double d2, d3, d4;
    //double d1; // 같은 이름의 메모리를 중복하여 준비할 수 없다.
    
    //3) 논리값을 저장할 메모리 준비 
    boolean b1;
    boolean b2, b3;
    
    //4) 문자열을 저장할 메모리 준비
    String s1;
    String s2, s3, s4;
  }
}










